-- =================================================================
-- إصلاح تصنيفات الحسابات لضمان توازن الميزانية العمومية
-- التاريخ: 01 إبريل 2026
-- المشكلة: حسابات التسوية (421) قد لا تظهر في قائمة الدخل مما يسبب فرقاً في الميزانية
-- =================================================================

-- 1. التأكد من وجود حساب فروقات التسوية (421) وتصحيح نوعه ليصبح "إيراد"
-- هذا سيجعل الـ 2000 تدخل ضمن صافي الدخل وتزيد حقوق الملكية، فتتزن الميزانية.
DO $$
BEGIN
    -- تحديث النوع إلى إيراد (Revenue) إذا كان الحساب موجوداً
    UPDATE public.accounts 
    SET type = 'Revenue' 
    WHERE code = '421';

    -- 2. التأكد من حساب تسوية العجز (541) وتصحيحه ليصبح "مصروف"
    UPDATE public.accounts 
    SET type = 'Expense' 
    WHERE code = '541';

    -- 3. التأكد من حساب ضريبة القيمة المضافة (2231) ليصبح "التزام"
    UPDATE public.accounts 
    SET type = 'Liability' 
    WHERE code = '2231';
    
    -- 4. التأكد من حساب الموردين وأوراق الدفع
    UPDATE public.accounts SET type = 'Liability' WHERE code IN ('201', '222');

END $$;

SELECT '✅ تم تحديث تصنيفات الحسابات. يرجى تحديث تقرير الميزانية الآن.' as status;